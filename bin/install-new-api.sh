#!/usr/bin/env sh
set -eu

# First-time production deployment.
# Creates .env when missing, then starts new-api with Redis and PostgreSQL.

ROOT_DIR="$(CDPATH= cd -- "$(dirname -- "$0")/.." && pwd)"
COMPOSE_FILE="${COMPOSE_FILE:-docker-compose.yml}"
BUILD_IMAGE="${BUILD_IMAGE:-1}"

cd "$ROOT_DIR"

log() {
  printf '[install-new-api] %s\n' "$*"
}

die() {
  printf '[install-new-api] ERROR: %s\n' "$*" >&2
  exit 1
}

random_secret() {
  if command -v openssl >/dev/null 2>&1; then
    openssl rand -hex 32
  elif [ -r /dev/urandom ]; then
    LC_ALL=C tr -dc 'A-Za-z0-9' </dev/urandom | head -c 64
    printf '\n'
  else
    date +%s | sha256sum | awk '{print $1}'
  fi
}

if command -v docker >/dev/null 2>&1 && docker compose version >/dev/null 2>&1; then
  COMPOSE="docker compose"
elif command -v docker-compose >/dev/null 2>&1; then
  COMPOSE="docker-compose"
else
  die "Docker Compose is not installed or not available in PATH"
fi

if [ ! -f "$COMPOSE_FILE" ]; then
  die "Compose file not found: $COMPOSE_FILE"
fi

mkdir -p data logs

if [ -f ".env" ]; then
  log ".env already exists; keeping existing production settings"
else
  log "Creating .env with generated production secrets"
  cat >.env <<EOF
# Generated by bin/install-new-api.sh.
# Keep this file private. Do not commit production secrets.

NEW_API_POSTGRES_USER=new_api
NEW_API_POSTGRES_PASSWORD=$(random_secret)
NEW_API_POSTGRES_DB=new-api
NEW_API_REDIS_PASSWORD=$(random_secret)

# Optional app settings.
SESSION_SECRET=$(random_secret)
TZ=Asia/Shanghai
EOF
fi

COMPOSE_ARGS="-f $COMPOSE_FILE"
UP_ARGS="-d"

if [ "$BUILD_IMAGE" = "1" ]; then
  UP_ARGS="$UP_ARGS --build"
fi

log "Starting Redis, PostgreSQL, and new-api"
# shellcheck disable=SC2086
$COMPOSE $COMPOSE_ARGS up $UP_ARGS redis postgres new-api

log "Current containers"
# shellcheck disable=SC2086
$COMPOSE $COMPOSE_ARGS ps

log "Done. View logs with: $COMPOSE $COMPOSE_ARGS logs -f new-api"
